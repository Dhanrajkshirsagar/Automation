package com.livehealth.model;

public class TestList {

	private String testName;

	private String testAmount;

	private String testStatus;

	private String testBillingDate;

	public TestList() {

	}

	public String getTestName() {
		return testName;
	}

	public void setTestName(String testName) {
		this.testName = testName;
	}

	public String getTestAmount() {
		return testAmount;
	}

	public void setTestAmount(String testAmount) {
		this.testAmount = testAmount;
	}

	public String getTestStatus() {
		return testStatus;
	}

	public void setTestStatus(String testStatus) {
		this.testStatus = testStatus;
	}

	public String getTestBillingDate() {
		return testBillingDate;
	}

	public void setTestBillingDate(String testBillingDate) {
		this.testBillingDate = testBillingDate;
	}

	
}
